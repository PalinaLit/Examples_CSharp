//  1. По двум заданным числам проверять 
// является ли первое квадратом второго

int a = 5;      // Вводим первую переменную
int b = 20;      // Вводим вторую перменную

if(b == a * a)      // Условие – если б является квадратом числа а – действие
{
    Console.WriteLine("Yes");       // Если условие правдиво
}
else
{
    Console.WriteLine("No");        // По-другому, то есть если условие ложно
}

