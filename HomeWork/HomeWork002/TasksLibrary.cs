    /// вторую цифру этого числа.
    /// 456 -> 5;
    /// 782 -> 8;
    /// 918 -> 1;
    /// </summary>
    public static void Task010()
    {
        // Вывод условия для пользователя
        Console.WriteLine("Определим вторую цифру заданного трехзначного числа");
        // 1. Принимаем от пользователя трехзначное число
        int number = NumberInput("Введите трехзначное число");
        // 2. Преобразуем число пользователя в строку
        string numberConvert = stringNumber(number);
        // 3. Определяем вторую цифру числа как элемент строки 
       string result = SymbolNumber(1, numberConvert);
        // 4. Выводим вторую цифру числа
        Console.WriteLine(Printout("Вторая цифрв вашего числа -> ", result));
    }

