    /// <summary>
    /// Напишите программу, которая принимает на вход цифру, 
    /// обозначающую день недели, и проверяет, является ли этот день выходным.
    /// 6, 7 - выходной.
    /// НО дня с индексом 0 или 12 не существует.
    /// 6 -> да;
    /// 7 -> да;
    /// 1 -> нет;
    /// </summary>
    public static void Task015()
    {
        // Вывод условия для пользователя
        Console.WriteLine("Проверим является ли день недели выходным");
        Console.WriteLine("Понедельник - 1");
        Console.WriteLine("Вторник - 2");
        Console.WriteLine("Среда - 3");
        Console.WriteLine("Четверг - 4");
        Console.WriteLine("Пятница - 5");
        Console.WriteLine("Суббота - 6");
        Console.WriteLine("Воскресенье - 7");
        // 1. Пользователь вводи цифру 
        int dayNumber = NumberInput("Введите цифру обозначающую один из дней недели");
        // 2. Обозначаем условие:
        // входит ли введенная пользователем цифра в диапазон [6-7]
        if(dayNumber>=6 && dayNumber<=7)
        {
            Console.WriteLine("Да, это выходной день");
        }
        else
        {
